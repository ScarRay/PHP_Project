
<article><h2>Résumé</h2>
<h3>Bonjour !</h3>
<p>Je suis Paul "windoZ" C. et voici mon e-portefolio.</p>
<p>Tout les outils sur ce site ont été développé par moi et sont utilisables par tous.<br/>
Quel est l'objectif de ce site ?<br/>
L'objectif est de faire des expériences concrètes, et de les montrer. 
Quels sont les objectifs de ces outils ?
<ul>
<li>Mini-chat : fera office de "chat-box" pour tous les utilisateurs présents.</li>
<li>Articles : permettra de créer et de voir des annonces.</li>
<li>Séries : permettra de regrouper des vidéos embed afin d'en faire une galerie. Les utilisateurs pourront ajouter des URL vidéo embed ou non.</li>
<li>Citizen News : permettra de consulter des sites d'informations sans changer de page.</li>
<li>CV : permettra de générer et afficher son CV en ligne parmis d'autres CV.</li>
</ul>
Quelle est l'avancé de ces outils ?<br/>
Vous pouvez consulter la progression des outils de manière visuelle au bas de cette page.
<ul>
<li>Mini-chat : Celui-ci n'affiche pour le moment que les 10 derniers messages. Il est possible d'y écrire un message en renseignant un pseudo et un texte.</li>
<li>Articles : L'outil ne permet seulement que de consulter en liste, tous les articles présents dans la base de données.</li>
<li>Séries : L'un des plus avancé, il permet déjà d'établir une galerie des séries se trouvant en base de données, mais aussi d'y enregistrer des vidéos en donnant un URL embed.</li>
<li>Citizen News : Permet actuellement d'afficher la liste des [5;20] derniers articles d'un site d'informations via son flux RSS. Lorsqu'on clique sur un article, celui-ci s'affiche dans une iFrame en dessous. Certains sites ne permettent pas d'afficher cette iFrame (<a href="https://fr.wikipedia.org/wiki/Content_Security_Policy">CSP</a>). Il n'est pas encore dynamique et ne permet pas l'ajout de flux RSS à la volée.</li>
<li>CV : N'est pas encore commencé.</li>
</ul>
Quels sont les langages utilisés ?<br/>
Les langages utilisés sont :
<ul>
<li>PHP</li>
<li>MySQL</li>
<li>JavaScript : avec utilisation de jQuery et bientôt AngularJS.</li>
<li>HTML</li>
<li>CSS : avec utilisation de Bootstrap.</li></ul>

